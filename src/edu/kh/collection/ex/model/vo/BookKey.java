package edu.kh.collection.ex.model.vo;

public class BookKey {
	
	
	// 필드
	
	private String bookNum = "bookNum" ;
	private String bookName = "bookName"; 
	private String author = "author"; 
	private String price = "price"; 
	private String publisher = "publisher";
	
	
	
	public BookKey(String bookNum, String bookName, String author, String price, String publisher) {
		super();
		this.bookNum = bookNum;
		this.bookName = bookName;
		this.author = author;
		this.price = price;
		this.publisher = publisher;
	}
	
	
	
	
	
	

}
